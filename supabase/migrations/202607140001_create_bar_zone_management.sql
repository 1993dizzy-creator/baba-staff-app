create or replace function public.bar_update_zone(
  p_code text,
  p_expected_version integer,
  p_actor_user_id bigint,
  p_actor_name text,
  p_update_notes boolean,
  p_note_ko text,
  p_note_vi text,
  p_update_assignee boolean,
  p_assignee_user_id bigint,
  p_update_color boolean,
  p_color_key text
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
declare
  v_zone public.bar_zones%rowtype;
  v_new_assignee bigint;
  v_old_color text;
  v_staff_name text;
  v_changed boolean := false;
  v_action text;
begin
  select * into v_zone from public.bar_zones where code = p_code for update;
  if not found then
    return jsonb_build_object('status', 'not_found');
  end if;
  if v_zone.version <> p_expected_version then
    return jsonb_build_object('status', 'conflict', 'version', v_zone.version);
  end if;

  v_new_assignee := case when p_update_assignee then p_assignee_user_id else v_zone.assignee_user_id end;

  if p_update_notes and (v_zone.note_ko is distinct from p_note_ko or v_zone.note_vi is distinct from p_note_vi) then
    insert into public.bar_activity_logs (
      entity_type, entity_id, entity_code_snapshot, action_type,
      before_data, after_data, actor_user_id, actor_name_snapshot
    ) values (
      'zone', v_zone.id, v_zone.code, 'zone_content_updated',
      jsonb_build_object('note_ko', v_zone.note_ko, 'note_vi', v_zone.note_vi),
      jsonb_build_object('note_ko', p_note_ko, 'note_vi', p_note_vi),
      p_actor_user_id, p_actor_name
    );
    v_changed := true;
  end if;

  if p_update_assignee and v_zone.assignee_user_id is distinct from p_assignee_user_id then
    v_action := case
      when v_zone.assignee_user_id is null then 'zone_assignee_assigned'
      when p_assignee_user_id is null then 'zone_assignee_removed'
      else 'zone_assignee_changed'
    end;
    insert into public.bar_activity_logs (
      entity_type, entity_id, entity_code_snapshot, action_type,
      before_data, after_data, actor_user_id, actor_name_snapshot
    ) values (
      'zone', v_zone.id, v_zone.code, v_action,
      jsonb_build_object('assignee_user_id', v_zone.assignee_user_id),
      jsonb_build_object('assignee_user_id', p_assignee_user_id),
      p_actor_user_id, p_actor_name
    );
    v_changed := true;
  end if;

  if p_update_color then
    if v_new_assignee is null then
      raise exception 'An assignee is required to update a BAR staff color';
    end if;
    -- Serialize color changes for the same employee even when callers edit different zones.
    perform pg_catalog.pg_advisory_xact_lock(
      pg_catalog.hashtextextended('bar_staff_profile:' || v_new_assignee::text, 0)
    );
    select color_key into v_old_color from public.bar_staff_profiles where user_id = v_new_assignee;
    if v_old_color is distinct from p_color_key then
      select coalesce(nullif(name, ''), nullif(full_name, ''), username)
      into v_staff_name from public.users where id = v_new_assignee;
      insert into public.bar_staff_profiles (user_id, color_key, updated_by_user_id)
      values (v_new_assignee, p_color_key, p_actor_user_id)
      on conflict (user_id) do update set
        color_key = excluded.color_key,
        updated_by_user_id = excluded.updated_by_user_id,
        updated_at = now();
      insert into public.bar_activity_logs (
        entity_type, entity_id, entity_code_snapshot, action_type, before_data, after_data,
        actor_user_id, actor_name_snapshot
      ) values (
        'staff_profile', v_new_assignee, v_staff_name, 'staff_color_changed',
        jsonb_build_object('color_key', v_old_color),
        jsonb_build_object('color_key', p_color_key),
        p_actor_user_id, p_actor_name
      );
      v_changed := true;
    end if;
  end if;

  if v_changed then
    update public.bar_zones set
      note_ko = case when p_update_notes then p_note_ko else note_ko end,
      note_vi = case when p_update_notes then p_note_vi else note_vi end,
      assignee_user_id = case when p_update_assignee then p_assignee_user_id else assignee_user_id end,
      version = version + 1,
      updated_at = now(),
      updated_by_user_id = p_actor_user_id
    where id = v_zone.id
    returning version into p_expected_version;
  end if;

  return jsonb_build_object(
    'status', 'ok',
    'version', case when v_changed then p_expected_version else v_zone.version end,
    'changed', v_changed
  );
end;
$$;

create or replace function public.bar_update_zone_photo(
  p_code text,
  p_expected_version integer,
  p_image_path text,
  p_actor_user_id bigint,
  p_actor_name text
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, public
as $$
declare
  v_zone public.bar_zones%rowtype;
  v_action text;
  v_new_version integer;
begin
  select * into v_zone from public.bar_zones where code = p_code for update;
  if not found then
    return jsonb_build_object('status', 'not_found');
  end if;
  if v_zone.version <> p_expected_version then
    return jsonb_build_object('status', 'conflict', 'version', v_zone.version);
  end if;
  if v_zone.image_path is not distinct from p_image_path then
    return jsonb_build_object('status', 'ok', 'version', v_zone.version, 'changed', false, 'old_image_path', v_zone.image_path);
  end if;

  v_action := case
    when v_zone.image_path is null then 'zone_photo_added'
    when p_image_path is null then 'zone_photo_removed'
    else 'zone_photo_replaced'
  end;

  update public.bar_zones set
    image_path = p_image_path,
    version = version + 1,
    updated_at = now(),
    updated_by_user_id = p_actor_user_id
  where id = v_zone.id
  returning version into v_new_version;

  insert into public.bar_activity_logs (
    entity_type, entity_id, entity_code_snapshot, action_type,
    before_data, after_data, actor_user_id, actor_name_snapshot
  ) values (
    'zone', v_zone.id, v_zone.code, v_action,
    jsonb_build_object('image_path', v_zone.image_path),
    jsonb_build_object('image_path', p_image_path),
    p_actor_user_id, p_actor_name
  );

  return jsonb_build_object(
    'status', 'ok', 'version', v_new_version, 'changed', true,
    'old_image_path', v_zone.image_path
  );
end;
$$;

revoke all on function public.bar_update_zone(text, integer, bigint, text, boolean, text, text, boolean, bigint, boolean, text) from public, anon, authenticated;
revoke all on function public.bar_update_zone_photo(text, integer, text, bigint, text) from public, anon, authenticated;
grant execute on function public.bar_update_zone(text, integer, bigint, text, boolean, text, text, boolean, bigint, boolean, text) to service_role;
grant execute on function public.bar_update_zone_photo(text, integer, text, bigint, text) to service_role;
