-- BAR zone content, staff colors, append-only activity logs, and atomic mutations.
-- All access is through server route handlers using the service role. No client RLS policies are created.

create table public.bar_zones (
  id bigint generated by default as identity primary key,
  code text not null unique,
  kind text not null,
  selectable_for_keeping boolean not null,
  note_ko text,
  note_vi text,
  image_path text,
  assignee_user_id bigint references public.users(id) on delete set null,
  is_active boolean not null default true,
  version integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  updated_by_user_id bigint references public.users(id) on delete set null,
  constraint bar_zones_kind_check check (kind in ('storage', 'equipment')),
  constraint bar_zones_code_check check (
    code in ('A1', 'A2', 'A3', 'B1', 'B2', 'B3', 'B4', 'B5', 'B6', 'B7', 'B8', 'C1', 'C2', 'C3', 'C4', 'C5', 'C6', 'C7', 'C8')
  ),
  constraint bar_zones_semantics_check check (
    (code = 'A2' and kind = 'equipment' and selectable_for_keeping = false)
    or
    (code <> 'A2' and kind = 'storage' and selectable_for_keeping = true)
  ),
  constraint bar_zones_version_check check (version >= 1)
);

create index bar_zones_assignee_user_id_idx on public.bar_zones(assignee_user_id);
create index bar_zones_is_active_idx on public.bar_zones(is_active);

create table public.bar_staff_profiles (
  user_id bigint primary key references public.users(id) on delete cascade,
  color_key text not null,
  updated_by_user_id bigint references public.users(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint bar_staff_profiles_color_key_check check (
    color_key in ('red', 'orange', 'yellow', 'green', 'teal', 'blue', 'purple', 'pink')
  )
);

create table public.bar_activity_logs (
  id bigint generated by default as identity primary key,
  entity_type text not null,
  entity_id bigint not null,
  entity_code_snapshot text,
  action_type text not null,
  before_data jsonb,
  after_data jsonb,
  actor_user_id bigint references public.users(id) on delete set null,
  actor_name_snapshot text not null,
  created_at timestamptz not null default now()
);

create index bar_activity_logs_recent_idx on public.bar_activity_logs(created_at desc, id desc);
create index bar_activity_logs_entity_idx on public.bar_activity_logs(entity_type, entity_id, created_at desc);
create index bar_activity_logs_actor_idx on public.bar_activity_logs(actor_user_id);
create index bar_activity_logs_action_idx on public.bar_activity_logs(action_type);

alter table public.bar_zones enable row level security;
alter table public.bar_staff_profiles enable row level security;
alter table public.bar_activity_logs enable row level security;

