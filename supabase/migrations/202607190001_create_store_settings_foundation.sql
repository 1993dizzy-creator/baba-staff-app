create table public.store_setting_versions (
  id bigint generated by default as identity primary key,
  timezone text not null,
  business_day_cutoff_time time without time zone not null,
  effective_from_business_date date not null,
  revision bigint not null,
  state text not null default 'active',
  created_by bigint not null references public.users(id),
  created_at timestamptz not null default now(),
  cancelled_by bigint null references public.users(id),
  cancelled_at timestamptz null,
  cancel_reason text null,
  constraint store_setting_versions_timezone_check check (timezone = 'Asia/Ho_Chi_Minh'),
  constraint store_setting_versions_revision_check check (revision > 0),
  constraint store_setting_versions_state_check check (state in ('active', 'cancelled')),
  constraint store_setting_versions_cancel_state_check check (
    (state = 'active' and cancelled_by is null and cancelled_at is null and cancel_reason is null)
    or (state = 'cancelled' and cancelled_by is not null and cancelled_at is not null)
  ),
  constraint store_setting_versions_revision_unique unique (revision)
);

create table public.store_business_hours (
  id bigint generated by default as identity primary key,
  setting_version_id bigint not null references public.store_setting_versions(id) on delete cascade,
  weekday smallint not null,
  is_closed boolean not null default false,
  open_time time without time zone null,
  close_time time without time zone null,
  created_at timestamptz not null default now(),
  constraint store_business_hours_weekday_check check (weekday between 0 and 6),
  constraint store_business_hours_weekday_unique unique (setting_version_id, weekday),
  constraint store_business_hours_values_check check (
    (is_closed and open_time is null and close_time is null)
    or (not is_closed and open_time is not null and close_time is not null and open_time <> close_time)
  )
);

create table public.store_setting_audit_logs (
  id bigint generated by default as identity primary key,
  setting_version_id bigint not null references public.store_setting_versions(id),
  action text not null,
  actor_user_id bigint not null references public.users(id),
  before_snapshot jsonb null,
  after_snapshot jsonb null,
  created_at timestamptz not null default now(),
  constraint store_setting_audit_logs_action_check check (action in ('created', 'cancelled'))
);

create index store_setting_versions_active_effective_idx
