create table public.attendance_record_manual_overrides (
  id bigint generated by default as identity primary key,
  attendance_record_id bigint not null
    references public.attendance_records(id) on delete cascade,
  override_metric text not null,
  override_action text not null default 'normalize',
  actor_user_id bigint not null references public.users(id),
  reason text null,
  created_at timestamptz not null default now(),
  revoked_at timestamptz null,
  revoked_by bigint null references public.users(id),
  revoke_reason text null,
  constraint attendance_record_manual_overrides_metric_check
    check (override_metric in ('late', 'early_leave')),
  constraint attendance_record_manual_overrides_action_check
    check (override_action in ('normalize'))
);

create unique index attendance_record_manual_overrides_active_metric_unique
  on public.attendance_record_manual_overrides (
    attendance_record_id,
    override_metric
  )
  where revoked_at is null;

create index attendance_record_manual_overrides_record_created_idx
  on public.attendance_record_manual_overrides (
    attendance_record_id,
    created_at desc
  );

alter table public.attendance_record_manual_overrides enable row level security;

revoke all on table public.attendance_record_manual_overrides
  from public, anon, authenticated, service_role;
revoke all on sequence public.attendance_record_manual_overrides_id_seq
  from public, anon, authenticated, service_role;
grant select, insert, update on table public.attendance_record_manual_overrides
  to service_role;
grant usage, select on sequence public.attendance_record_manual_overrides_id_seq
  to service_role;

comment on table public.attendance_record_manual_overrides is
  'Current, explicit administrator overrides for individual attendance metrics. Historical records are intentionally not inferred or backfilled.';
comment on column public.attendance_record_manual_overrides.override_metric is
  'The independently overridden metric, currently late or early_leave.';
comment on column public.attendance_record_manual_overrides.revoked_at is
  'When set, this override no longer excludes the metric from policy comparison.';

create or replace function public.attendance_admin_normalize_late_v1(
  p_attendance_record_id bigint,
  p_actor_user_id bigint,
  p_reason text default null
)
returns jsonb
language plpgsql
security invoker
set search_path = pg_catalog, public
as $$
declare
  v_record public.attendance_records%rowtype;
  v_after public.attendance_records%rowtype;
begin
  select *
    into v_record
  from public.attendance_records
  where id = p_attendance_record_id
  for update;

  if not found then
    return jsonb_build_object('status', 'record_changed');
  end if;

  if v_record.status = 'leave' or v_record.check_in_at is null then
    return jsonb_build_object('status', 'late_cannot_be_normalized');
  end if;

  if coalesce(v_record.late_minutes, 0) <= 0 then
    return jsonb_build_object(
      'status', 'no_op',
      'record', to_jsonb(v_record)
    );
  end if;

  update public.attendance_records
  set
    late_minutes = 0,
    status = case
      when status = 'early_leave' or coalesce(early_leave_minutes, 0) > 0
        then 'early_leave'
      when check_out_at is not null then 'done'
      else 'working'
    end,
    updated_at = now()
  where id = v_record.id
    and coalesce(late_minutes, 0) > 0
  returning * into v_after;

  if not found then
    return jsonb_build_object('status', 'record_changed');
  end if;

  insert into public.attendance_record_manual_overrides (
    attendance_record_id,
    override_metric,
    override_action,
    actor_user_id,
    reason
  )
  values (
    v_record.id,
    'late',
    'normalize',
    p_actor_user_id,
    nullif(btrim(p_reason), '')
  );

  insert into public.attendance_record_audit_logs (
    attendance_record_id,
    source_attendance_record_id,
    target_user_id,
    work_date,
    action,
    actor_user_id,
    before_snapshot,
    after_snapshot,
    reason
  )
  values (
    v_after.id,
    v_record.id,
    v_record.user_id,
    v_record.work_date,
    'normalize_late',
    p_actor_user_id,
    to_jsonb(v_record),
    to_jsonb(v_after),
    nullif(btrim(p_reason), '')
  );

  return jsonb_build_object(
    'status', 'ok',
    'record', to_jsonb(v_after)
  );
end
$$;

revoke execute on function public.attendance_admin_normalize_late_v1(
  bigint,
  bigint,
  text
) from public, anon, authenticated;
grant execute on function public.attendance_admin_normalize_late_v1(
  bigint,
  bigint,
  text
) to service_role;

